package com.hehaoyisheng.bcgame.entity;

public class Proxy {
    private int id;
    private String shangji;
    private String xiaji;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getShangji() {
        return shangji;
    }

    public void setShangji(String shagnji) {
        this.shangji = shagnji;
    }

    public String getXiaji() {
        return xiaji;
    }

    public void setXiaji(String xiaji) {
        this.xiaji = xiaji;
    }
}
